    private void salvarRegistro() {
        if (!validarCampos()) {
            return;
        }
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja salvar?", "Confirmação",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        }
        RegistroAutoclave registro = new RegistroAutoclave();

        if (!jTextFieldCodigoRegistro.getText().isEmpty()) {
            autoclave.setId(Integer.parseInt(jTextFieldCodigoRegistro.getText()));
        }
        registro.setDataRegistro(jFormattedTextFieldDataRegistro.getText());
        registro.setDataDesbloqueio(jFormattedTextFieldDataDesbloqueio.getText());

        registro.setRevenda(revenda);
        registro.setClienteFinal(clienteFinal);

        registro.setAutoclave(autoclave);

        registro.setStatus(jComboBoxStatus.getSelectedItem().toString());

        Usuario user = (Usuario) HashSistema.hash.get("usuarioLogado");
        String ip = pegarIPDaMaquina();
        Log log = new Log(user, Utilidades.retData(), "Desktop", "Inserto/Update - Registro: " + autoclave.getNumeroSerie(), ip);

        TransactionManager.beginTransaction();
        try {
            RegistroAutoclaveDao tDao = new RegistroAutoclaveDao();
            new LogDao().persistir(log);
            tDao.persistir(registro);

            TransactionManager.commit();

            JOptionPane.showMessageDialog(null, "Registro foi gravado!");
            carregarTabelaRegistrosBloqueados();
            carregarTabelaRegistrosDesbloqueados();
            limparFormulario();
        } catch (Exception e) {
            TransactionManager.rollback();
            JOptionPane.showMessageDialog(null, "Registro não pode ser gravado!" + e);
        }
    }

