    private void carregarTabelaRegistrosBloqueados() {
        RegistroAutoclaveDao dGP = new RegistroAutoclaveDao();
        registrosBloqueados = dGP.obterLista("status ='Bloqueado'");
        mostrarRegistros(registrosBloqueados);
    }

    private void mostrarRegistros(List<RegistroAutoclave> registros) {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Cod", "Registro", "Revenda", "Cliente Final", "Autoclave", "Numero de Serie", "Codigo de Desbloqueio", "Data desbloqueio"});


        Collections.reverse(registros);
        if (registros != null) {
            for (RegistroAutoclave registro : registros) {
                String idRegistro = String.valueOf(registro.getId());
                String nomeRevenda = registro.getRevenda().getNome();
                String nomeClienteFinal = buscarNomeClienteFinal(registro);
                model.addRow(new Object[]{idRegistro, registro.getDataRegistro(), nomeRevenda, nomeClienteFinal, registro.getAutoclave().getNomeAutoclave(), registro.getAutoclave().getNumeroSerie(), registro.getAutoclave().getCodigoDesbloqueio(), registro.getDataDesbloqueio()});
            }
            jTableBloqueados.setModel(model);
            definirTamanhoDasColunas(jTableBloqueados);
        }
    }

    private void definirTamanhoDasColunas(JTable jTableBloqueados) {
        jTableBloqueados.getColumnModel().getColumn(0).setPreferredWidth(4);
        jTableBloqueados.getColumnModel().getColumn(1).setPreferredWidth(70);
    }

    private void carregarTabelaRegistrosDesbloqueados() {
        RegistroAutoclaveDao dGP = new RegistroAutoclaveDao();
        registrosDesbloqueados = dGP.obterLista("status ='Desbloqueado'");
        mostrarRegistrosDesbloqueados(registrosDesbloqueados);
    }

