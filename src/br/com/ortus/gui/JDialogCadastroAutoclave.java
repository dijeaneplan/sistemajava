    private void salvarAutoclave() {
         if (!validarCampos()) {
            return;
        }
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja salvar?", "ConfirmaÃ§Ã£o",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        }
        Autoclave autoclave = new Autoclave();
        if (!jTextFieldCodigoAutoclave.getText().isEmpty()) {
            autoclave.setId(Integer.parseInt(jTextFieldCodigoAutoclave.getText()));
        }
        autoclave.setNomeAutoclave(jComboBoxModelo.getSelectedItem().toString());
        autoclave.setNumeroSerie(jTextFieldNumeroSerie.getText());
        autoclave.setCodigoDesbloqueio(jTextFieldCodigoDesbloqueio.getText());
        
        //expedicao.setDataExpedicao(new Utilidades().calendarToString(Calendar.getInstance()));
        //autoclave.setDataExpedicao(jFormattedTextData.getText());
        
        Usuario user = (Usuario) HashSistema.hash.get("usuarioLogado");
        String ip = pegarIPDaMaquina();
        Log log = new Log(user, Utilidades.retData(), "Desktop", "Inserto/Update - AUTOCLAVE: "+ autoclave.getId() + " - " + autoclave.getNumeroSerie() + " - " + autoclave.getCodigoDesbloqueio(), ip);
        
        



        TransactionManager.beginTransaction();
        try {
            AutoclaveDao tDao = new AutoclaveDao();
            new LogDao().persistir(log);
            tDao.persistir(autoclave);

            TransactionManager.commit();

            JOptionPane.showMessageDialog(null, "Autoclave foi gravada!");
            carregarTabelaAutoclave();
            limparFormulario();
        } catch (Exception e) {
            TransactionManager.rollback();
            JOptionPane.showMessageDialog(null, "Autoclave nÃ£o pode ser gravado!" + e);
        }
    }

    private void carregarTabelaAutoclave() {
        AutoclaveDao dGP = new AutoclaveDao();
        autoclaves = dGP.obterLista();
        mostrarAutoclaves(autoclaves);
    }

    private void mostrarAutoclaves(List<Autoclave> autoclaves) {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Cod", "Autoclave", "NÃºmero de SÃ©rie", "CÃ³digo de Desbloqueio"});


        Collections.reverse(autoclaves);
        if (autoclaves != null) {
            for (Autoclave autoclave : autoclaves) {
                model.addRow(new Object[]{autoclave.getId(),autoclave.getNomeAutoclave(), autoclave.getNumeroSerie(), autoclave.getCodigoDesbloqueio()});
            }
            jTableAutoclaves.setModel(model);
            definirTamanhoDasColunas(jTableAutoclaves);
        }
        //Collections.reverse(trocasEnviadas);
        //jTextFieldQtdeItens.setText(String.valueOf(autoclaves.size()));
    }

    private void definirTamanhoDasColunas(JTable tabela) {
        tabela.getColumnModel().getColumn(0).setPreferredWidth(4);
        tabela.getColumnModel().getColumn(1).setPreferredWidth(250);
    }

    private void pintarTabela() {
        jTableAutoclaves.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {
                super.getTableCellRendererComponent(table, value, isSelected,
                        hasFocus, row, column);
                if (row % 2 == 0) {
                    setBackground(new Color(204, 255, 204));
                    setForeground(Color.BLACK);
                } else if (row % 2 == 1) {
                    setBackground(new Color(232, 255, 232));
                    setForeground(Color.BLACK);
                } 
                return this;
            }
        });
    }

    private boolean validarCampos() {
         int erros = 0;
        if (jTextFieldNumeroSerie.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O numero de serie deve ser preenchido!");
            erros++;
        }
        if (erros != 0) {
            return false;
        }
        return true;
    }

    private String pegarIPDaMaquina() {
        try {
            return InetAddress.getLocalHost().getHostAddress() + " - " + InetAddress.getLocalHost().getHostName();
        } catch (Exception e) {
            System.out.println(e);
            return " . . . ";
        }
    }

    private void editarAutoclave() {
        int linha = jTableAutoclaves.getSelectedRow();
        if (linha != -1) {
            Autoclave autoclave = autoclaves.get(linha);
            preencherFormularioComDados(autoclave);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para editar.");
            return;
        }
    }

    private void preencherFormularioComDados(Autoclave autoclave) {
        jTextFieldCodigoAutoclave.setText(String.valueOf(autoclave.getId()));
        jTextFieldNumeroSerie.setText(autoclave.getNumeroSerie());
        jTextFieldCodigoDesbloqueio.setText(autoclave.getCodigoDesbloqueio());
        jComboBoxModelo.setSelectedIndex(buscarNomeAutoclave(autoclave.getNomeAutoclave()));
    }

    private int buscarNomeAutoclave(String nome) {
        int count = 0;
        while (count < jComboBoxModelo.getItemCount()) {
            if (jComboBoxModelo.getItemAt(count).toString().equals(nome)) {
                return count;
            }
            count++;
        }
        return count;
    }

    private void pegarAutoclaveParaTelaDeRegistro() {
        int linha = jTableAutoclaves.getSelectedRow();
        if (linha != -1) {
            Autoclave autoclave = autoclaves.get(linha);
            HashSistema.hash.put("autoclave", autoclave);
            dispose();
            //TransactionManager.commit();
        } else {
            JOptionPane.showMessageDialog(null, "Voce deve selecionar uma linha!");
            return;
        }
    }
