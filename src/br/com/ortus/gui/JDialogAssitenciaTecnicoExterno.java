    private void atualizarNumeroDecaracteresAreaTexto() {
        int qtdeCaracteres = jTextAreaReclamacao.getText().length();
        if (qtdeCaracteres > 1000) {
            jLabelcontroleCaracter.setText(String.valueOf(qtdeCaracteres));
            jLabelcontroleCaracter.setForeground(Color.red);
            jTextAreaReclamacao.setBackground(new Color(252, 196, 196));
        } else {
            jLabelcontroleCaracter.setText(String.valueOf(qtdeCaracteres));
            jLabelcontroleCaracter.setForeground(new Color(7, 129, 17));
            jTextAreaReclamacao.setBackground(Color.WHITE);
        }
    }

    private void pintarTabelaComponentes() {
        jTableTabelaComponentes.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {
                super.getTableCellRendererComponent(table, value, isSelected,
                        hasFocus, row, column);
                if (row % 2 == 0) {
                    setBackground(new Color(204, 255, 204));
                    setForeground(Color.BLACK);
                } else if (row % 2 == 1) {
                    setBackground(new Color(232, 255, 232));
                    setForeground(Color.BLACK);
                }
                return this;
            }
        });
    }

